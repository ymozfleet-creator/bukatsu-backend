function exportWeeklyReportPDF() {
  var team = getMyTeam();
  if (!team) { toast('チーム情報がありません','e'); return; }
  var pls = DB.players.filter(function(p){return p.team===team.id;});
  if (!pls.length) { toast('選手がいません','w'); return; }

  try {
    var jspdf = window.jspdf;
    if (!jspdf) { toast('PDF生成ライブラリを読み込み中...','i'); return; }
    var doc = new jspdf.jsPDF({orientation:'portrait',unit:'mm',format:'a4'});

    var now = new Date();
    var weekAgo = new Date(); weekAgo.setDate(now.getDate()-7);
    var period = weekAgo.toLocaleDateString('ja-JP')+'〜'+now.toLocaleDateString('ja-JP');

    // Header
    doc.setFontSize(18);
    doc.setTextColor(30,50,86);
    doc.text(sanitize(team.name,25)+' Weekly Report', 20, 25);
    doc.setFontSize(11);
    doc.setTextColor(120,150,188);
    doc.text(period, 20, 33);
    doc.setDrawColor(14,165,233);
    doc.setLineWidth(0.5);
    doc.line(20, 37, 190, 37);

    // Gather stats
    var stats = {sessions:0, avgCond:0, condN:0, pain:0, attT:0, attP:0};
    var rows = [];
    pls.forEach(function(p){
      var tLog=DB.trainingLog[p.id]||{};var cLog=DB.conditionLog[p.id]||{};
      var s=0,cS=0,cN=0,hp=false;
      for(var i=0;i<7;i++){var d=new Date();d.setDate(now.getDate()-i);var ds=d.toISOString().slice(0,10);
        Object.values(tLog).forEach(function(e){if(e.date===ds)s++;});
        if(cLog[ds]){if(cLog[ds].mood){cS+=cLog[ds].mood;cN++;}if(cLog[ds].pain)hp=true;}
      }
      stats.sessions+=s;if(cN){stats.avgCond+=cS;stats.condN+=cN;}if(hp)stats.pain++;
      rows.push([sanitize(p.name,14),sanitize(p.pos||'--',8),String(s),cN?String(Math.round(cS/cN*10)/10):'--',hp?'Yes':'-']);
    });

    var avgC = stats.condN ? Math.round(stats.avgCond/stats.condN*10)/10 : '--';

    // KPI boxes
    var y = 45;
    doc.setFillColor(240,246,255);
    [[20,'Sessions',String(stats.sessions)],[65,'Avg Condition',String(avgC)],[110,'Pain Reports',String(stats.pain)],[155,'Players',String(pls.length)]].forEach(function(kpi){
      doc.roundedRect(kpi[0],y,40,20,3,3,'F');
      doc.setFontSize(16);doc.setTextColor(30,50,86);doc.text(kpi[2],kpi[0]+20,y+10,{align:'center'});
      doc.setFontSize(8);doc.setTextColor(120,150,188);doc.text(kpi[1],kpi[0]+20,y+16,{align:'center'});
    });

    // Table
    y = 75;
    doc.setFontSize(12);doc.setTextColor(30,50,86);doc.text('Player Summary',20,y);
    y += 8;
    doc.setFillColor(241,245,249);doc.rect(20,y,170,8,'F');
    doc.setFontSize(9);doc.setTextColor(100,116,139);
    ['Name','Pos','Sessions','Condition','Pain'].forEach(function(h,i){
      doc.text(h, 22+i*34, y+5.5);
    });
    y += 10;
    doc.setTextColor(51,65,85);
    rows.forEach(function(row){
      if(y>270){doc.addPage();y=20;}
      row.forEach(function(cell,i){doc.text(cell,22+i*34,y+4);});
      doc.setDrawColor(226,232,240);doc.line(20,y+6,190,y+6);
      y += 8;
    });

    // Footer
    doc.setFontSize(8);doc.setTextColor(148,163,184);
    doc.text('Generated by MyCOACH-MyTEAM | '+now.toLocaleDateString('ja-JP'),20,285);

    doc.save(sanitize(team.name,15)+'_weekly_'+now.toISOString().slice(0,10)+'.pdf');
    toast('PDFをダウンロードしました','s');
  } catch(e) {
    console.error('PDF error:',e);
    toast('PDF生成に失敗しました','e');
  }
}

// ── Phase3: ダッシュボード用アクティビティタイムライン ──────────
function renderActivityTimeline() {
  var activities = [];
  var now = new Date();
  var cutoff = new Date(); cutoff.setDate(now.getDate() - 7);
  var teamId = _getMyTeamId();
  if (!teamId) return '';

  // Gather recent activities
  (DB.announcements||[]).filter(function(a){return a.teamId===teamId && a.createdAt && new Date(a.createdAt)>=cutoff;})
    .forEach(function(a){activities.push({time:a.createdAt,icon:'📢',bg:'rgba(249,115,22,.1)',text:'アナウンス「'+sanitize(a.title||'',20)+'」を投稿',type:'announce'});});

  _dbArr('requests').filter(function(r){return r.teamId===teamId && r.createdAt && new Date(r.createdAt)>=cutoff;})
    .forEach(function(r){
      var label = r.status==='contracted'?'本契約成立':r.status==='matched'?'マッチング成立':r.status==='pending'?'マッチング申請':'';
      if(label) activities.push({time:r.contractedAt||r.createdAt,icon:r.status==='contracted'?'📝':'🤝',bg:r.status==='contracted'?'rgba(0,207,170,.1)':'rgba(14,165,233,.1)',text:sanitize(r.coachName||'コーチ',12)+' '+label,type:'match'});
    });

  _dbArr('payments').filter(function(p){return p.team===teamId && p.paidAt && new Date(p.paidAt)>=cutoff;})
    .forEach(function(p){
      var pl = DB.players.find(function(x){return x.id===p.player;});
      activities.push({time:p.paidAt,icon:'💴',bg:'rgba(34,197,94,.1)',text:sanitize(pl?.name||'選手',12)+' 月謝支払い完了',type:'payment'});
    });

  (DB.events||[]).filter(function(e){
    if(!e.createdAt) return false;
    return e.teamId===teamId && new Date(e.createdAt)>=cutoff;
  }).forEach(function(e){
    activities.push({time:e.createdAt,icon:'📅',bg:'rgba(168,85,247,.1)',text:'予定「'+sanitize(e.title||'',18)+'」を追加',type:'calendar'});
  });

  if (!activities.length) return '';

  activities.sort(function(a,b){return b.time>a.time?1:-1;});
  activities = activities.slice(0, 8);

  var html = '<div class="dash-section"><div class="dash-section-head"><span class="dash-section-title">📰 最近のアクティビティ</span></div>';
  activities.forEach(function(a) {
    var timeStr = '';
    try {
      var d = new Date(a.time);
      var diff = Math.round((now - d) / 3600000);
      timeStr = diff < 1 ? 'たった今' : diff < 24 ? diff+'時間前' : Math.floor(diff/24)+'日前';
    } catch(e){ timeStr = ''; }
    html += '<div class="timeline-item">';
    html += '<div class="timeline-dot" style="background:'+a.bg+'">'+a.icon+'</div>';
    html += '<div class="timeline-content"><div class="timeline-text">'+a.text+'</div>';
    html += '<div class="timeline-meta">'+timeStr+'</div></div></div>';
  });
  html += '</div>';
  return html;
}

