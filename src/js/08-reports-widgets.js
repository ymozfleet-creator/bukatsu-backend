// ── Phase3b: スワイプ操作 ──────────────────────────────────────
function initSwipeHandler() {
  var startX=0,startY=0,swiping=false;
  var pb=document.getElementById('page-body');
  if(!pb) return;
  pb.addEventListener('touchstart',function(e){
    if(e.touches.length!==1) return;
    startX=e.touches[0].clientX;startY=e.touches[0].clientY;swiping=true;
  },{passive:true});
  pb.addEventListener('touchend',function(e){
    if(!swiping) return; swiping=false;
    var endX=e.changedTouches[0].clientX,endY=e.changedTouches[0].clientY;
    var dx=endX-startX,dy=endY-startY;
    if(Math.abs(dx)<60||Math.abs(dy)>Math.abs(dx)*0.7) return;
    var pg=window.currentPage||'';
    if(pg==='calendar'||pg==='schedule'){
      if(dx<0){calOffset++;goTo('calendar');}
      else{calOffset--;goTo('calendar');}
    }
  },{passive:true});
}

