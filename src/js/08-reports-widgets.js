function teamFilesPage(){
  if(DB.currentUser?.role!=='team'){return '<div class="card text-center" style="padding:40px">🔒 チーム管理者のみ利用できます</div>';}
  var team=getMyTeam();
  if(!team) return '<div class="card text-center" style="padding:40px">チーム情報が見つかりません</div>';
  var files=_dbArr('teamFiles').filter(function(f){return f.teamId===team.id;});
  var cats=['練習資料','戦術ボード','試合映像','トレーニング動画','ミーティング','その他'];
  var curCat=window._tfCat||'all';
  var filtered=curCat==='all'?files:files.filter(function(f){return f.category===curCat;});
  filtered.sort(function(a,b){return(b.createdAt||'')>(a.createdAt||'')?1:-1;});

  var html='<div class="pg-head flex justify-between items-center"><div><div class="pg-title">📂 資料・動画共有</div><div class="pg-sub">チームメンバーとコーチに資料を共有</div></div>'
    +'<button class="btn btn-primary btn-sm" onclick="openAddTeamFile()">＋ 資料を追加</button></div>';

  // Category filter
  html+='<div style="display:flex;gap:6px;flex-wrap:wrap;margin-bottom:16px">'
    +'<button class="btn '+(curCat==='all'?'btn-primary':'btn-ghost')+' btn-sm" onclick="window._tfCat=\'all\';refreshPage()">全て <span style="font-size:10px;opacity:.7">'+files.length+'</span></button>';
  cats.forEach(function(c){
    var cnt=files.filter(function(f){return f.category===c;}).length;
    if(cnt>0) html+='<button class="btn '+(curCat===c?'btn-primary':'btn-ghost')+' btn-sm" onclick="window._tfCat=\''+c+'\';refreshPage()">'+c+' <span style="font-size:10px;opacity:.7">'+cnt+'</span></button>';
  });
  html+='</div>';

  if(filtered.length===0){
    html+=emptyState('📁','まだ資料がありません','「＋ 資料を追加」から動画リンクや画像を共有しましょう','＋ 資料を追加',"openAddTeamFile()");
  } else {
    html+='<div style="display:grid;grid-template-columns:repeat(auto-fill,minmax(280px,1fr));gap:14px">';
    filtered.forEach(function(f){
      var typeIcon=f.type==='video'?'🎬':f.type==='image'?'🖼️':f.type==='link'?'🔗':'📄';
      var thumb='';
      if(f.type==='video'&&f.url){
        var ytId=_extractYoutubeId(f.url);
        if(ytId) thumb='<div style="width:100%;aspect-ratio:16/9;background:url(https://img.youtube.com/vi/'+ytId+'/mqdefault.jpg) center/cover;border-radius:8px 8px 0 0"></div>';
        else thumb='<div style="width:100%;aspect-ratio:16/9;background:linear-gradient(135deg,#1e3a5f,#0ea5e9);border-radius:8px 8px 0 0;display:flex;align-items:center;justify-content:center;font-size:40px;color:#fff">🎬</div>';
      } else if(f.type==='image'&&f.data){
        thumb='<div style="width:100%;aspect-ratio:16/9;background:url('+f.data+') center/cover;border-radius:8px 8px 0 0"></div>';
      } else {
        thumb='<div style="width:100%;height:60px;background:var(--surf2);border-radius:8px 8px 0 0;display:flex;align-items:center;justify-content:center;font-size:28px">'+typeIcon+'</div>';
      }
      html+='<div class="card" style="padding:0;overflow:hidden;cursor:pointer" onclick="openTeamFileDetail(\''+f.id+'\')">'
        +thumb
        +'<div style="padding:14px 16px">'
        +'<div class="flex items-center gap-6 mb-4"><span style="font-size:16px">'+typeIcon+'</span><span class="fw7" style="font-size:14px;flex:1;overflow:hidden;text-overflow:ellipsis;white-space:nowrap">'+sanitize(f.title||'無題',30)+'</span></div>'
        +'<div style="font-size:12px;color:var(--txt2);margin-bottom:6px">'+sanitize(f.category||'',10)+' · '+(f.createdAt||'').slice(0,10)+'</div>'
        +(f.description?'<div style="font-size:12px;color:var(--txt2);line-height:1.5;display:-webkit-box;-webkit-line-clamp:2;-webkit-box-orient:vertical;overflow:hidden">'+sanitize(f.description,80)+'</div>':'')
        +'<div class="flex gap-4 mt-8"><button class="btn btn-ghost btn-xs" style="font-size:12px" onclick="event.stopPropagation();editTeamFile(\''+f.id+'\')">✏️ 編集</button><button class="btn btn-ghost btn-xs" style="color:var(--red);font-size:12px" onclick="event.stopPropagation();deleteTeamFile(\''+f.id+'\')">🗑️ 削除</button></div>'
        +'</div></div>';
    });
    html+='</div>';
  }
  return html;
}

