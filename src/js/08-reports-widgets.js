async function handlePaymentReturn(){
  const params = new URLSearchParams(location.search);
  const status   = params.get('payment');
  const sessionId= params.get('session_id');
  const threadId = params.get('threadId');

  if(!status) return; // 通常のアクセス

  // URLパラメータをクリア（リロードで再処理しないよう）
  history.replaceState({}, '', location.pathname);

  if(status === 'success' && sessionId){
    try {
      // バックエンドからセッション情報を取得して確認
      const res = await fetch(`${API_BASE}/session-status/${sessionId}`, {headers: await _apiHeaders()});
      const data = await res.json();

      if(data.status === 'paid'){
        const meta = data.metadata || {};

        // ローカルDBを更新（Webhookも更新するが、UIの即時反映用）
        if(meta.type === 'coach_fee' && meta.threadId){
          const pt = _dbArr('payThreads').find(p => p.id === meta.threadId);
          if(pt){
            const inv = pt.invoices.find(i => i.month === meta.month && i.status !== 'paid');
            if(inv){
              inv.status  = 'paid';
              inv.paidAt  = new Date().toLocaleDateString('ja-JP');
              inv.stripeSessionId = sessionId;
              const _hprIso=new Date().toISOString();
              pt.messages.push({
                from:'system',
                teamText: `${inv.month}分 ¥${fmtNum(inv.teamPays)} の支払いが完了しました。`,
                coachText:`${inv.month}分 ¥${fmtNum(inv.coachReceives)} が入金されました。`,
                text:`${inv.month}分の支払いが完了しました。`,
                time: new Date().toLocaleString('ja-JP'),
                isoTime: _hprIso
              });
              _dbArr('coachPay').push({id:'cp'+Date.now(), coach:pt.coachId, team:pt.teamId,
                amount:Number(meta.originalAmount), month:meta.month, status:'paid',
                teamPays:Number(meta.originalAmount), coachReceives:Math.round(Number(meta.originalAmount)*.9),
                teamFee:Number(meta.platformFee), coachFee:Number(meta.platformFee),
                platformTotal:Number(meta.platformFee)*2});
              // コーチに通知
              const _hprCoach=DB.coaches.find(c=>c.id===pt.coachId);
              const _hprTeam=DB.teams.find(t=>t.id===pt.teamId);
              addNotif(`✅ ${_hprTeam?.name||'チーム'}から${inv.month}分の支払いが完了（¥${fmtNum(inv.coachReceives)}）`,'fa-check-circle','threads',pt.coachId);
              // チャットルームに通知
              const _hprCk=pt.chatKey||Object.keys(DB.chats).find(k=>{const c=DB.chats[k];return c.type==='match'&&c.coachId===pt.coachId&&c.teamId===pt.teamId;});
              if(_hprCk&&DB.chats[_hprCk]){
                DB.chats[_hprCk].msgs.push({mid:_genMsgId(),from:'system',name:'システム',text:`✅ ${inv.month}分の指導料支払いが完了しました。`,time:now_time(),date:_hprIso.slice(0,10),read:false});
              }
            }
          }
        }

        if(meta.type === 'monthly_fee'){
          // playerId（バックエンドのmetadata）で月謝レコードを検索
          const pay = _dbArr('payments').find(p =>
            p.player === meta.playerId && p.team === meta.teamId && (p.month === meta.month || !meta.month)
          );
          if(pay){
            pay.status = 'paid';
            pay.paidAt = new Date().toISOString();
            pay.stripeSessionId = sessionId;
            pay.method = 'card';
            pay.fee = Number(meta.platformFee) || 0;
          }
        }

        if(meta.type === 'adhoc' && meta.invoiceId){
          const inv = _dbArr('adhocInvoices').find(i => i.id === meta.invoiceId);
          if(inv){
            inv.status = 'paid';
            inv.paidAt = new Date().toISOString();
            inv.stripeSessionId = sessionId;
            inv.fee = Number(meta.platformFee) || 0;
          }
        }

        saveDB();
        // 決済証跡を永続記録
        if(typeof PaymentTrail !== 'undefined') PaymentTrail.record('charge', {amount:Number(meta.originalAmount||0), playerId:meta.playerId||'', teamId:meta.teamId||'', coachId:meta.coachId||'', month:meta.month||'', sessionId:sessionId, status:'paid', note:'Stripe Checkout完了'});
        addNotif('💳 決済が完了しました', 'fa-check-circle', 'threads');
        toast('✅ 決済が完了しました！', 's');
        if(meta.threadId) setTimeout(()=>{ goTo('threads'); openThreadDetail(meta.threadId); }, 500);
        else if(meta.type === 'monthly_fee') goTo('parent-fee');
        else goTo('dashboard');

      } else if(data.status === 'unpaid'){
        // コンビニ・銀行振込: 入金待ち状態
        addNotif('🏪 決済手続き完了 — コンビニ/銀行振込での入金をお待ちしています', 'fa-clock', 'threads');
        toast('📩 決済手続きが完了しました。コンビニ/銀行振込での入金が確認でき次第、ステータスが更新されます。', 'i', 6000);
        goTo('dashboard');

      } else {
        toast('決済は完了していません（ステータス: ' + data.status + '）', 'w');
      }
    } catch(err) {
      /* session check error suppressed */;
      // バックエンド未接続でもUIには成功表示
      toast('決済確認中にエラーが発生しました。しばらくしてからご確認ください。', 'w');
    }

  } else if(status === 'cancelled'){
    toast('決済がキャンセルされました', 'w');
    if(threadId) setTimeout(()=>{ goTo('threads'); openThreadDetail(threadId); }, 500);
  }
}

