function renderActivityTimeline() {
  var activities = [];
  var now = new Date();
  var cutoff = new Date(); cutoff.setDate(now.getDate() - 7);
  var teamId = _getMyTeamId();
  if (!teamId) return '';

  // Gather recent activities
  (DB.announcements||[]).filter(function(a){return a.teamId===teamId && a.createdAt && new Date(a.createdAt)>=cutoff;})
    .forEach(function(a){activities.push({time:a.createdAt,icon:'📢',bg:'rgba(249,115,22,.1)',text:'アナウンス「'+sanitize(a.title||'',20)+'」を投稿',type:'announce'});});

  _dbArr('requests').filter(function(r){return r.teamId===teamId && r.createdAt && new Date(r.createdAt)>=cutoff;})
    .forEach(function(r){
      var label = r.status==='contracted'?'本契約成立':r.status==='matched'?'マッチング成立':r.status==='pending'?'マッチング申請':'';
      if(label) activities.push({time:r.contractedAt||r.createdAt,icon:r.status==='contracted'?'📝':'🤝',bg:r.status==='contracted'?'rgba(0,207,170,.1)':'rgba(14,165,233,.1)',text:sanitize(r.coachName||'コーチ',12)+' '+label,type:'match'});
    });

  _dbArr('payments').filter(function(p){return p.team===teamId && p.paidAt && new Date(p.paidAt)>=cutoff;})
    .forEach(function(p){
      var pl = DB.players.find(function(x){return x.id===p.player;});
      activities.push({time:p.paidAt,icon:'💴',bg:'rgba(34,197,94,.1)',text:sanitize(pl?.name||'選手',12)+' 月謝支払い完了',type:'payment'});
    });

  (DB.events||[]).filter(function(e){
    if(!e.createdAt) return false;
    return e.teamId===teamId && new Date(e.createdAt)>=cutoff;
  }).forEach(function(e){
    activities.push({time:e.createdAt,icon:'📅',bg:'rgba(168,85,247,.1)',text:'予定「'+sanitize(e.title||'',18)+'」を追加',type:'calendar'});
  });

  if (!activities.length) return '';

  activities.sort(function(a,b){return b.time>a.time?1:-1;});
  activities = activities.slice(0, 8);

  var html = '<div class="dash-section"><div class="dash-section-head"><span class="dash-section-title">📰 最近のアクティビティ</span></div>';
  activities.forEach(function(a) {
    var timeStr = '';
    try {
      var d = new Date(a.time);
      var diff = Math.round((now - d) / 3600000);
      timeStr = diff < 1 ? 'たった今' : diff < 24 ? diff+'時間前' : Math.floor(diff/24)+'日前';
    } catch(e){ timeStr = ''; }
    html += '<div class="timeline-item">';
    html += '<div class="timeline-dot" style="background:'+a.bg+'">'+a.icon+'</div>';
    html += '<div class="timeline-content"><div class="timeline-text">'+a.text+'</div>';
    html += '<div class="timeline-meta">'+timeStr+'</div></div></div>';
  });
  html += '</div>';
  return html;
}

