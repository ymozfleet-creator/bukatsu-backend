async function verifyAdminPin(pin){
  if(_pinAttempts.lockUntil>Date.now()){
    toast('PINロック中。'+Math.ceil((_pinAttempts.lockUntil-Date.now())/60000)+'分後に再試行','e'); return false;
  }
  var stored=localStorage.getItem('mc_admin_pin_hash');
  if(!stored){ await _initAdminPin(); stored=localStorage.getItem('mc_admin_pin_hash'); }
  if(stored&&stored.startsWith('pinv2$')){
    var hash=await _hashPin(pin);
    if(hash===stored){_pinAttempts={count:0,lockUntil:0};return true;}
  }
  if(stored&&!stored.startsWith('pinv2$')){
    var lh=await _hashPinLegacy(pin);
    if(lh===stored){var nh=await _hashPin(pin);localStorage.setItem('mc_admin_pin_hash',nh);_pinAttempts={count:0,lockUntil:0};return true;}
  }
  _pinAttempts.count++;
  if(_pinAttempts.count>=PIN_MAX_ATTEMPTS){
    _pinAttempts.lockUntil=Date.now()+PIN_LOCKOUT_MS;_pinAttempts.count=0;
    if(typeof addAuditLog==='function') addAuditLog('admin_pin_lockout','PINロック');
    toast('PIN '+PIN_MAX_ATTEMPTS+'回失敗。5分間ロック。','e');
  }
  return false;
}
