// 管理者PIN認証設定
// 管理者PIN: PBKDF2ハッシュ + レガシー互換
async function _hashPin(pin){
  try {
    var enc = new TextEncoder();
    var keyMaterial = await crypto.subtle.importKey('raw', enc.encode(pin), 'PBKDF2', false, ['deriveBits']);
    var bits = await crypto.subtle.deriveBits({name:'PBKDF2', salt:enc.encode('mycoach_admin_pin_v2'), iterations:50000, hash:'SHA-256'}, keyMaterial, 256);
    return 'pinv2$' + Array.from(new Uint8Array(bits)).map(b=>b.toString(16).padStart(2,'0')).join('');
  } catch(e) {
    var buf = await crypto.subtle.digest('SHA-256', new TextEncoder().encode(pin+'_mc_pin_salt_v2'));
    return 'pinv2$' + Array.from(new Uint8Array(buf)).map(b=>b.toString(16).padStart(2,'0')).join('');
  }
}
async function _hashPinLegacy(pin){
  var buf = await crypto.subtle.digest('SHA-256', new TextEncoder().encode(pin+'_mc_pin_salt'));
  return Array.from(new Uint8Array(buf)).map(b=>b.toString(16).padStart(2,'0')).join('');
}
