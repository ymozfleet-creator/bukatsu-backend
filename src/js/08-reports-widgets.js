// ── モバイル表示自動修正 ──
function _mobileFixLayout(){
  if(window.innerWidth > 768) return;
  const pb = document.getElementById('page-body');
  if(!pb) return;
  
  // 1. 全インラインgridを1カラムに強制
  pb.querySelectorAll('[style]').forEach(el => {
    const s = el.style;
    if(s.gridTemplateColumns && s.gridTemplateColumns !== '1fr'){
      const cols = s.gridTemplateColumns;
      // 2カラム以上は1frに (ただしstat-rowの2カラムは維持)
      if(cols.includes('1fr 1fr 1fr') || cols.includes('repeat(3') || cols.includes('repeat(4') ||
         cols.includes('repeat(5') || cols.includes('repeat(6') || cols.includes('repeat(8') ||
         /\d{3,}px/.test(cols)) {
        s.gridTemplateColumns = '1fr';
      }
      // 2カラムで片方が固定幅(200px以上)の場合も1frに
      if(/\d{3,}px\s+1fr|1fr\s+\d{3,}px/.test(cols)){
        s.gridTemplateColumns = '1fr';
      }
    }
    // 2. flex-wrapを強制
    if(s.display === 'flex' && s.gap && !s.flexWrap){
      s.flexWrap = 'wrap';
    }
    // 3. 大きすぎるpadding縮小
    const pad = parseInt(s.padding);
    if(pad > 28) s.padding = '14px';
    // 4. 固定widthが画面超えないように
    const w = parseInt(s.width);
    if(w > 300 && !s.maxWidth) s.maxWidth = '100%';
    const minW = parseInt(s.minWidth);
    if(minW > 200) s.minWidth = 'unset';
  });
  
  // 5. overflow防止
  pb.querySelectorAll('.page, .card, .stat-box, .thread-card, .coach-card, .tm-card').forEach(el => {
    el.style.overflow = 'hidden';
    el.style.maxWidth = '100%';
  });
  
  // 6. 長い名前/テキストにellipsis
  pb.querySelectorAll('.fw7, .pg-title').forEach(el => {
    if(el.scrollWidth > el.clientWidth + 5){
      el.style.overflow = 'hidden';
      el.style.textOverflow = 'ellipsis';
      el.style.whiteSpace = 'nowrap';
    }
  });
}

