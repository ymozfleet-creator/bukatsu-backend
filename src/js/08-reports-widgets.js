function submitTrialLesson(coachId, reqId) {
  var valid = validateForm([['trial-date', {required:true, requiredMsg:'日付を選択してください'}]]);
  if (!valid) return;
  var date = document.getElementById('trial-date')?.value||'';
  var time = document.getElementById('trial-time')?.value||'';
  var place = (document.getElementById('trial-place')?.value||'').trim();
  var note = (document.getElementById('trial-note')?.value||'').trim();
  var online = document.getElementById('trial-online')?.checked||false;
  var coach = DB.coaches.find(function(c){return c.id===coachId;});
  var team = getMyTeam();
  if (!coach||!team) { toast('エラー','e'); return; }

  if (!DB.trialLessons) DB.trialLessons = [];
  var trialId = genId('trial');
  DB.trialLessons.push({
    id:trialId, coachId:coachId, teamId:team.id, reqId:reqId||'',
    date:date, time:time, place:place?sanitize(place,60):'',
    note:note?sanitize(note,200):'', online:online,
    status:'scheduled', createdAt:new Date().toISOString(), createdBy:DB.currentUser?.id
  });

  var d = new Date(date);
  DB.events.push({
    id:genId('ev'), title:'🎯 トライアル: '+sanitize(coach.name,12),
    date:d.getDate(), month:d.getMonth(), year:d.getFullYear(),
    time:time, type:'match',
    note:(place?'📍 '+place+' ':'')+(online?'💻 オンライン':''),
    teamId:team.id, trialId:trialId, scope:'team',
    createdAt:new Date().toISOString(), createdBy:DB.currentUser?.id
  });

  var chatKey = Object.keys(DB.chats).find(function(k){return DB.chats[k].reqId===reqId&&DB.chats[k].type==='match';});
  if (chatKey&&DB.chats[chatKey]) {
    DB.chats[chatKey].msgs.push({
      mid:_genMsgId(),from:'system',name:'システム',
      text:'🎯 トライアルレッスンが設定されました！\n📅 '+date+(time?' '+time:'')+(place?'\n📍 '+place:'')+(online?'\n💻 オンライン':'')+(note?'\n📝 '+note:''),
      time:now_time(),date:new Date().toISOString().slice(0,10),read:false
    });
  }
  addNotif(sanitize(team.name,12)+'がトライアルを依頼',  'fa-flask','chat',coachId);
  saveDB(); closeM();
  toast('トライアルレッスンを設定しました！','s');
  refreshPage();
}

