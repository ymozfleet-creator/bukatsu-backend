function exportPlayersCSV(){
  if(!DB.currentUser||!["team","admin","coach"].includes(DB.currentUser.role)){toast("権限がありません","e");return;}
  var team=getMyTeam();
  if(!team){toast('チーム情報がありません','e');return;}
  var pls=DB.players.filter(function(p){return p.team===team.id;});
  if(!pls.length){toast('選手がいません','w');return;}

  var headers=['名前','ポジション','年齢','背番号','ステータス','体調','保護者','登録日'];
  var rows=[headers.join(',')];
  pls.forEach(function(p){
    var guard=p.guardianId?_getUsers().find(function(u){return u.id===p.guardianId;}):null;
    var conds=(DB.conditions||[]).filter(function(c){return c.player===p.id;}).sort(function(a,b){return b.date>a.date?1:-1;});
    var lastCond=conds.length?conds[0].score:'--';
    rows.push([
      '"'+(p.name||'').replace(/"/g,'""')+'"',
      p.pos||'',
      p.age||'',
      p.number||'',
      p.status||'active',
      lastCond,
      '"'+(guard?.name||'未設定').replace(/"/g,'""')+'"',
      p.createdAt?p.createdAt.slice(0,10):''
    ].join(','));
  });
  var bom='\uFEFF';
  var blob=new Blob([bom+rows.join('\n')],{type:'text/csv;charset=utf-8'});
  var a=document.createElement('a');
  a.href=URL.createObjectURL(blob);
  a.download=sanitize(team.name,20)+'_選手一覧_'+new Date().toISOString().slice(0,10)+'.csv';
  a.click();
  toast('📥 選手一覧CSVをダウンロードしました','s');
}

