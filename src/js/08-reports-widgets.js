// ── Helper: YouTube ID抽出 ──
function _extractYoutubeId(url){
  if(!url)return null;
  var m=url.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/|youtube\.com\/embed\/)([a-zA-Z0-9_-]{11})/);
  return m?m[1]:null;
}

